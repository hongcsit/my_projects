CREATE TABLE ACCOUNT
(ACC_ID CHAR(4),
CUST_ID CHAR(4),
BRANCH_ID CHAR(4),
ACC_BALANCE DECIMAL(8,2),
ACC_TYPE CHAR(1),
FOREIGN KEY (CUST_ID) REFERENCES CUSTOMER (CUST_ID),
FOREIGN KEY (BRANCH_ID) REFERENCES BANK_BRANCH (BRANCH_ID),
PRIMARY KEY (ACC_ID, CUST_ID));




